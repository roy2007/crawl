
package org.crawl.http.payload.web.utils;

/**
 *
 * @author Roy
 * @date 2021/01/11
 */

public class MatchingObject {

    int mark;

    String data;
    String dataAds;

    public int getMark() {
        return mark;
    }

    public String getData() {
        return data;
    }

    public String getDataAds() {
        return dataAds;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }

    public void setData(String data) {
        this.data = data;
    }

    public void setDataAds(String dataAds) {
        this.dataAds = dataAds;
    }
}
